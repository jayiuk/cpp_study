#include <iostream>
using namespace std;
int main(){
    int num1 = 10;
    int num2 = 4;
    cout << "+ 연산자에 의한 결과 : " << num1 + num2 << endl;
    cout << "- 연산자에 의한 결과 : " << num1 - num2 << endl;
    cout << "* 연산자에 의한 결과 : " << num1 * num2 << endl;
    cout << "/ 연산자에 의한 결과 : " << num1 / num2 << endl;
    cout << "% 연산자에 의한 결과 : " << num1 % num2 << endl;
    return 0;
}